package net.acednetwork.api.world;


public class AcedWorld {

private double x, y, z;
private boolean valid;
private int id;

public double getX() {
return x;
}
public double setX(double x) {
return this.x = x;
}
public double getY() {
return y;
}
public double setY(double y) {
return this.y = y;
}

public double getZ() {
return z;
}
public double setZ(double z) {
return this.z = z;
}
public boolean isValid() {
return valid;
}
public int getID() {
return id;
}
public int setID(int id) {
return this.id = id;
}

}


